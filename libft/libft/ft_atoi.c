#include "libft.h"

int			ft_atoi(const char *s)
{
	int		sign;
	long	r;

	r = 0;
	sign = 1;
	while (*s == 32 || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '-')
	{
		sign = -1;
	}
	if (*s == '+' || *s == '-')
		s++;
	while (*s >= '0' && *s <= '9')
	{
		r = r * 10 + *s - '0';
		s++;
		if (r > 9223372036854775807)
			return (sign > 0 ? -1 : 0);
	}
	return ((int)(sign * r));
}
