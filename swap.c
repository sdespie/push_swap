#include "push_swap.h"

void	ft_swap(int *a, int *b)
{
	int	tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

int		diff(int x, int y)
{
	if (x < y)
		return (y - x);
	return (x - y);
}
